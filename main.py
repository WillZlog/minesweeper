MINE = 9


def build_neighbors(side_length: int):
    """builds a list, where at index 0, returns all the neighbors of the tile at posititon 0. this list stays global and should be generated once

    Args:
        side_length (int): length of a side of the board

    Returns:
        list: a list with side_length**2 nested lists inside.
    """
    board_size = side_length * side_length
    neighbors = []

    for index in range(board_size):
        row = index // side_length
        col = index % side_length
        current = []

        for d_row in (-1, 0, 1):
            for d_col in (-1, 0, 1):
                if d_row == 0 and d_col == 0:
                    continue

                new_row = row + d_row
                new_col = col + d_col

                if 0 <= new_row < side_length and 0 <= new_col < side_length:
                    current.append(new_row * side_length + new_col)

        neighbors.append(current)

    return neighbors


def build_board(side_length: int, num_mines: int, neighbors: list):
    """builds the board, only called once at the begining of each game

    Args:
        side_length (int): length of each side of the board
        num_mines (int): number of mines on the board
        neighbors (list): generated neighbors list by build_neighbors(), see build_neighbors()

    Returns:
        list: generated board, should not be shown to or used by bot
    """
    board_size = side_length * side_length
    board = [0] * board_size

    mine_positions = random.sample(range(board_size), num_mines)

    for mine_index in mine_positions:
        board[mine_index] = MINE

    for mine_index in mine_positions:
        for neighbor in neighbors[mine_index]:
            if board[neighbor] != MINE:
                board[neighbor] += 1

    return board


def reveal_square(index: int, board: list, hidden: list, state: dict, num_mines: int):
    """reveals a square of the board and updates hidden and state based off board

    Args:
        index (int): index of tile to be revealed
        board (list): the actual minesweeper board
        hidden (list): the hidden list
        state (dict): state of game
        num_mines (int): number of mines on the board

    Returns:
        False | True | None | int: returns None if the game is already over, returns False if clicked tile is a mine, returns True if all safe tiles are clicked, and returns the value of the tile if none of the above.
    """
    if state["game_over"]:
        return None

    if not hidden[index]:
        return None

    hidden[index] = False

    if board[index] == MINE:
        state["game_over"] = True
        state["won"] = False
        return False

    state["safe_left"] -= 1

    if state["safe_left"] == 0:
        state["game_over"] = True
        state["won"] = True
        return True

    return board[index]


def flood_fill(
    start_index: int,
    board: list,
    hidden: list,
    state: dict,
    neighbors: list,
    num_mines: int,
    clues: dict | None = None,
):
    """if a 0 is clicked, click all surrounding tiles, and continue if any of those are zeros

    Args:
        start_index (int): index of the first tile to be clicked
        board (list): the non hidden board, with all values
        hidden (list): the hidden board, with only true or false, see smartishBot() for more info
        state (dict): the state of the game, see smartishBot()
        neighbors (list): generated by build_neighbors(), see build_neighbors()
        num_mines int): number of mines on the board
        clues (dict, optional): a dictionary of already revealed clues. Defaults to None (due to some bots not storing clues).
    """
    if state["game_over"]:
        return

    stack = [start_index]

    while stack and not state["game_over"]:
        current = stack.pop()

        for neighbor in neighbors[current]:
            if not hidden[neighbor]:
                continue

            result = reveal_square(neighbor, board, hidden, state, num_mines)

            if result is False or result is True:
                return
            elif result == 0:
                stack.append(neighbor)
            else:
                if clues is not None:
                    clues[neighbor] = result


def random_bot(
    side_length: int, num_mines: int, neighbors: list
):  # * this is a baseline bot that just gueses random indices every round
    start = time.perf_counter()
    board_size = side_length * side_length
    board = build_board(side_length, num_mines, neighbors)
    hidden = [True] * board_size
    state = {
        "game_over": False,
        "won": False,
        "safe_left": board_size - num_mines,
    }

    click_order = list(range(board_size))
    random.shuffle(click_order)

    for index in click_order:
        if state["game_over"]:
            break
        if not hidden[index]:
            continue

        result = reveal_square(index, board, hidden, state, num_mines)

        if result == 0:
            flood_fill(index, board, hidden, state, neighbors, num_mines)
    end = time.perf_counter()
    timeDelta = end - start
    return state["won"], timeDelta


